        <h1>TOEFL Format</h1>
        <p>TOEFL is administered online from where it gets its name - TOEFL iBT (Internet Based Test). The test is of four and a half hours duration and consists of a variety of reading, listening, speaking and writing tasks.
During the test, students will be expected to demonstrate their ability to combine all four skills: reading, listening, speaking and writing tasks. For instance, students may be asked to:</p>
        <ul>
            <li>Read, listen and then write in response to a question</li>
            <li>Read, listen and then speak in response to a question</li>
            <li>Listen and then speak in response to a question</li>
        </ul>
