        <h1>GRE Format</h1>
        <p>The GRE is a multiple-choice multi-stage test that measures verbal reasoning, quantitative reasoning, critical thinking and analytical writing skills. It has been designed in a way so as to calculate the student's intellectual and logical abilities, adapting to performance by selecting questions based on previous answers. The test allows its takers to skip questions within a section, go back and change answers and have the flexibility to choose which questions within a section they want to answer first.</p>
        <p>The GRE question types are designed to closely reflect the kind of thinking that students will be expected to do in graduate or business school and are based upon the following skills:</p>
        <ul>
            <li><b>Verbal Reasoning -</b> This section measures a student's ability to analyze and evaluate written material and to synthesize information obtained from it. It measures the ability to analyze relationships among component parts of sentences and recognize relationships among concepts and words.</li>
            <li><b>Quantitative Reasoning - </b>This section measures a student's problem-solving ability and focusses on basic concepts of algebra, arithmetic, data analysis and geometry.</li>
            <li><b>Analytical Writing -</b> This section measures analytical writing skills and critical thinking. It specifically measure's a student's ability to articulate and support ideas that are complex in nature both clearly and effectively.</li>
        </ul>
