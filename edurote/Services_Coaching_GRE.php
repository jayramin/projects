        <h2>GRE FAQs</h2>
        <h4>Q.Does the GRE General Test measure knowledge in any specific disciplines?</h4>
        <p>The GRE Practice Test measures a student's verbal and quantitative reasoning, critical thinking and analytical writing skills – disciplines that are relevant to every field of study and important in graduate or business schools.</p>
        
        <h4>Q.How is the GRE scored?</h4>
        <p>The first sections of the Verbal and Quantitative Reasoning parts range from the levels of 'Easy' to 'Difficult'. Each subsequent section is administered based on the student's overall performance in the preceding section. A score of 130 - 170 per section is generated for the Verbal and Quantitative sections. Both scores are then added to give a total score of 260 - 340.    </p>
        
        <h4>Q.What kind of writing software does the AWA section employ?</h4>
        <p>Students will use a basic word processor developed by ETS. The basic word processor contains a number of functionalities: insert text, delete text, cut-and-paste and undo the previous action. Other tools such as a grammar checker or a spell checker and are not available in the ETS software. This makes essay writing fair in relation to handwritten essays of the students in the paper-based tests.</p>
        
        <h4>Q.Is it advisable to re-take the GRE if the score received in the first attempt is low?</h4>
        <p>Students must make a concerted effort to prepare for the GRE the second time and retake the exam, if they feel that their GRE test scores do not reflect their actual capabilities.</p>
        
