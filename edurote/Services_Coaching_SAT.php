        <h1>SAT Scoring Methodology</h1>
        <p>Talking about relevant SAT information, two sections of SAT (Mathematics and Evidence-Based Reading and Writing) will be scored on a scale that ranges from 200-800 points giving a possible total of 1600. Sub scores and insight scores will also be reported along with an optional essay (scored separately).
Questions that a student does not answer do not count either for or against their score. Neither are points taken away for wrong answers on the math questions where the answers has to be entered into a grid. The raw score for each section is then converted into a scaled score. This is where the score of 200–800 points comes from, which is done through a statistical process called 'equating'.
</p>
        <h3>Key points to bear in mind when taking the New SAT:</h3>
        <ul>
            <li>As against to the old SAT guidelines, there is no negative marking or wrong-answer penalty.</li>
            <li>Too much time is often taken up answering specific questions, especially, in the Critical Reading section, which results in loss of valuable time to attempt other questions. Also, the critical reading section now does not asks for sentence completions.</li>
            <li>There are only 4 answer choices per question in the New SAT test format.</li>
            <li>Only a portion of questions focus on in-depth analysis of content and evidence</li>
            <li>SAT is available in print and digitally</li>
        </ul>
