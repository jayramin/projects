        <h2>Scoring Pattern</h2>
        <p>The PTE test is scored against the Global Scale of English, giving you an accurate snapshot of the performance of the test-taker on a scale of 10 - 90. Detailed score reports show the overall test-taker performance as well as their ability on sub-skills and enabling skills. Join a coaching centre and work hard to attain good PTE scores.</p>
        
         <h2>Item scoring</h2>
        <p>All items in PTE Academic are machine-scored. Scores for some item types are based on correctness alone, while others are based on correctness, formal aspects and the quality of the response.</p>
        <p>Formal aspects refer to the form of the response: for example, whether it is over or under the word limit for a particular item type. The quality of the response is represented in the enabling skills. For example, in the item type Re-tell lecture, the response is scored on skills such as oral fluency and pronunciation.</p>
        <p>Scores for item types assessing speaking and writing skills are generated by automated scoring systems. There are two types of scoring: Correct or Incorrect.</p>
        <p>Some item types are scored as either correct or incorrect. If a response is correct, a score of 1 is given, but if it is incorrect, no score points are awarded.</p>
        
         <h2>Partial credit</h2>
        <p>Other item types are scored as correct, partially correct or incorrect. If responses to these items are correct, the maximum score points available for each item type will be received, but if they are partly correct, some score points will be given, but less than the maximum available for the item type. If responses are incorrect, no score points will be received.</p>
        <p>The tables that follow give an overview of how the 20 item types in the three parts of PTE Academic are scored. They also show timings, the number of items in any given test, the communicative skills, enabling skills and other elements scored.</p>
        <p><b>Note-</b> The minimum and maximum time indicated for the sections of each part of the test do not add up to the total time stated. This is because different versions of the test are balanced for total length. No candidate will get the maximum or minimum times indicated.</p>
        <p> The PTE Academic reports an overall score.</p>
        
         <h2>Overall score</h2>
        <p>The overall score is based on the student's performance on all test items. For each item, the score you obtain contributes to the overall score. The overall score range is 10-90 points. Good preparation will definitely result in good PTE scores.</p>
        
