        <h2>IELTS FAQs</h2>
        <h4>Q. Why are there two versions of the IELTS test?</h4>
        <p>IELTS has two versions – Academic and General Training. The Academic test is for those who want to study at a tertiary level in an English-speaking country. The General Training test is for those who want to work or training programmes, secondary school or migrate to an English-speaking country. All candidates take the same Listening and Speaking tests, but different Reading and Writing tests.</p>
        
        <h4>Q. When can I take IELTS test?</h4>
        <p>IELTS is conducted once every week. You're advised to book an appointment in advance.</p>
        
        <h4>Q. My given and family names are the same. What should I enter into the registration form?</h4>
        <p>Enter your name into the family name field. Enter a period (.) in the given name field.</p>
        
        <h4>Q. What documents can I use as ID (proof of identity) at the test centers?</h4>
        <p>You need to produce a valid passport when you appear for the test.</p>
        
        <h4>Q. Who sets the 'pass' mark for the IELTS test?</h4>
        <p>There is no pass or fail in IELTS. Scores are graded on the 9-band system. Each educational institution or organization sets its own level of IELTS scores to meet its individual requirements. To find out more, search the list of organizations which accept IELTS scores.</p>
        
        <h4>Q. How soon can I re-appear for the IELTS test?</h4>
        <p>There is no restriction on when you can re-appear for the test. However, IELTS recommends that you reflect on your result and make additional preparations before attempting the test again.</p>
        
        <h4>Q. Is the IELTS test completed in one day</h4>
        <p>The Listening, Reading and Writing components of the test are always completed in a continuous sequence, with no break. Depending on the test centre, the Speaking test may be taken either seven days prior to or after the main test date.</p>
        
        <h4>Q. What is the IELTS Speaking test?</h4>
        <p>The Speaking test is a conversation with a certified IELTS Examiner. The Speaking test is made up of three sections. It is recorded on an audiocassette or a digital recorder.</p>
        
        <h4>Q. How is IELTS Writing marked?</h4>
        <p>Examiners award a band score for each of four criterion areas: Task Achievement (for Task 1), Task Response (for Task 2), Coherence and Cohesion, Lexical Resource, and Grammatical Range and Accuracy. The four criteria are equally weighted.</p>
        
        <h4>Q. How is IELTS Speaking marked?</h4>
        <p>Examiners award a band score for each criterion: Fluency and Coherence, Lexical Resource, Grammatical Range and Accuracy, and Pronunciation. The four criteria are equally weighted.</p>
        
        <h4>Q. How is IELTS marked?</h4>
        <p>Candidates receive scores on a Band Scale from 1 to 9. A profile score is reported for each skill. The four individual scores are averaged and rounded to produce an Overall Band Score. Overall Band Scores and scores for each sub-test (Listening, Reading, Writing and Speaking) are reported in whole bands or half bands.</p>
