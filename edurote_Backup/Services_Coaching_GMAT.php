        <h4>Q.What is a Computer Adaptive Test?</h4>
        <p>In a computer-adaptive test (CAT), questions are selected according to the student's level of intelligence. It starts with a question of average difficulty, and the correctness of the answer sets the difficulty level of the subsequent question. This pattern continues until the student gets to the end of the section and their ability on that subject has been measured.</p>
        
